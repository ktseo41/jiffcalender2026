#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const https = require('https');

const ROOT_DIR = path.resolve(__dirname, '..');
const OUTPUT_PATH = path.join(ROOT_DIR, 'jiff2026', 'directors.js');
const BASE_URL = 'https://www.jeonjufest.kr';
const TIMETABLE_DAY_NUMS = Array.from({ length: 10 }, (_, index) => index);
const REQUEST_HEADERS = {
  'User-Agent': 'Mozilla/5.0 (compatible; jiff-schedule-timeline/1.0)',
  Accept: 'text/html,application/xhtml+xml',
};

main().catch(error => {
  console.error(error.stack || error.message || error);
  process.exitCode = 1;
});

async function main() {
  const codeEntries = new Map();

  for (const dayNum of TIMETABLE_DAY_NUMS) {
    const html = await fetchText(`${BASE_URL}/Ticket/timetable_day.asp?dayNum=${dayNum}`);
    mergeCodeEntries(codeEntries, parseSchedulePage(html));
  }

  const movieIds = uniqueValues(
    Array.from(codeEntries.values()).flatMap(entry => entry.movieIds)
  );

  const movieDetails = await fetchMovieDetails(movieIds, 6);
  const byCode = {};

  Array.from(codeEntries.keys()).sort().forEach(code => {
    const entry = codeEntries.get(code);
    const detailEntries = entry.movieIds
      .map(movieId => movieDetails.get(movieId))
      .filter(Boolean);

    const directorDisplays = uniqueValues(detailEntries.map(detail => detail.directorLabel).filter(Boolean));
    const directorNames = uniqueValues(detailEntries.flatMap(detail => detail.directorNames));
    const detailMovieId = entry.directMovieId || (entry.movieIds.length === 1 ? entry.movieIds[0] : '');
    const detailCandidates = uniqueDetailCandidates(entry);

    byCode[code] = {
      movieIds: entry.movieIds,
      titles: entry.titles,
      detailMovieId,
      detailUrl: detailMovieId ? buildMovieUrl(detailMovieId) : '',
      detailCandidates,
      hasMultipleDetails: detailCandidates.length > 1,
      directorDisplays,
      directorLabel: directorDisplays.join(' · '),
      directorNames,
    };
  });

  const titleLookup = buildTitleLookup(byCode);
  const payload = {
    syncedAt: new Date().toISOString(),
    source: {
      timetableDayNums: TIMETABLE_DAY_NUMS,
      timetableUrl: `${BASE_URL}/Ticket/timetable_day.asp?dayNum={n}`,
      movieUrl: `${BASE_URL}/db/movieView.asp?idx={id}`,
    },
    byCode,
    byTitle: titleLookup.byTitle,
    byNormalizedTitle: titleLookup.byNormalizedTitle,
  };

  fs.writeFileSync(
    OUTPUT_PATH,
    [
      '// Generated by scripts/sync-jiff-directors.js',
      `window.JIFF_SCHEDULE_DIRECTORS = Object.freeze(${JSON.stringify(payload, null, 2)});`,
      '',
    ].join('\n'),
    'utf8'
  );

  console.log(
    `Wrote ${path.relative(ROOT_DIR, OUTPUT_PATH)} with ${Object.keys(byCode).length} schedule codes and ${movieDetails.size} movie detail pages.`
  );
}

function fetchText(url) {
  return new Promise((resolve, reject) => {
    const request = https.get(
      url,
      {
        headers: REQUEST_HEADERS,
        rejectUnauthorized: false,
      },
      response => {
        if (response.statusCode && response.statusCode >= 400) {
          response.resume();
          reject(new Error(`Request failed for ${url}: ${response.statusCode}`));
          return;
        }

        const chunks = [];
        response.setEncoding('utf8');
        response.on('data', chunk => chunks.push(chunk));
        response.on('end', () => resolve(chunks.join('')));
      }
    );

    request.on('error', reject);
  });
}

async function fetchMovieDetails(movieIds, batchSize) {
  const details = new Map();

  for (let start = 0; start < movieIds.length; start += batchSize) {
    const batch = movieIds.slice(start, start + batchSize);
    const results = await Promise.allSettled(batch.map(fetchMovieDetail));

    results.forEach((result, index) => {
      const movieId = batch[index];

      if (result.status === 'fulfilled') {
        details.set(movieId, result.value);
        return;
      }

      console.warn(`Failed to fetch ${movieId}: ${result.reason && result.reason.message ? result.reason.message : result.reason}`);
    });
  }

  return details;
}

async function fetchMovieDetail(movieId) {
  const html = await fetchText(`${BASE_URL}/db/movieView.asp?idx=${encodeURIComponent(movieId)}`);
  const directorNames = uniqueValues([
    ...extractDirectorBlockNames(html),
    ...extractDirectorCreditNames(html),
  ]);

  return {
    directorNames,
    directorLabel: formatDirectorLabel(html, directorNames),
  };
}

function parseSchedulePage(html) {
  const entries = new Map();
  const cardPattern = /<div class="screen-sort[\s\S]*?<div class="button-list">[\s\S]*?<\/div>\s*<\/div>\s*<\/div>/gi;
  const cards = html.match(cardPattern) || [];

  cards.forEach(cardHtml => {
    const code = cleanText(matchFirst(cardHtml, /<span class="number">([\s\S]*?)<\/span>/i));
    if (!code) return;

    const directMovieId = decodeHtml(
      matchFirst(cardHtml, /<div class="title">\s*<a href="\/db\/movieView\.asp\?idx=([^&"]*)/i)
    ).trim();
    const directTitle = cleanText(matchFirst(cardHtml, /<div class="title">(?:\s*<a [^>]*>)?([\s\S]*?)(?:<\/a>)?<\/div>/i));
    const optionPattern = /<option[^>]*value="([^"]*)"[^>]*>([\s\S]*?)<\/option>/gi;
    const movieIds = [];
    const titles = [];
    let optionMatch = optionPattern.exec(cardHtml);

    while (optionMatch) {
      const movieId = decodeHtml(optionMatch[1]).trim();
      const title = cleanText(optionMatch[2]);

      if (movieId) {
        movieIds.push(movieId);
        if (title) titles.push(title);
      }

      optionMatch = optionPattern.exec(cardHtml);
    }

    if (directMovieId) {
      movieIds.unshift(directMovieId);
    }

    if (directTitle) {
      titles.unshift(directTitle);
    }

    if (movieIds.length === 0 && !directMovieId) return;

    entries.set(code, {
      movieIds: uniqueValues(movieIds),
      titles: uniqueValues(titles),
      directMovieId,
    });
  });

  return entries;
}

function uniqueDetailCandidates(entry) {
  const candidates = [];

  if (entry.directMovieId) {
    candidates.push({
      title: entry.titles[0] || '',
      movieId: entry.directMovieId,
      url: buildMovieUrl(entry.directMovieId),
    });
  }

  entry.movieIds.forEach((movieId, index) => {
    candidates.push({
      title: entry.titles[index] || '',
      movieId,
      url: buildMovieUrl(movieId),
    });
  });

  const seen = new Set();

  return candidates.filter(candidate => {
    if (!candidate.movieId || seen.has(candidate.movieId)) return false;
    seen.add(candidate.movieId);
    return true;
  });
}

function mergeCodeEntries(target, source) {
  source.forEach((entry, code) => {
    const current = target.get(code) || { movieIds: [], titles: [], directMovieId: '' };
    current.movieIds = uniqueValues(current.movieIds.concat(entry.movieIds));
    current.titles = uniqueValues(current.titles.concat(entry.titles));
    current.directMovieId = current.directMovieId || entry.directMovieId || '';
    target.set(code, current);
  });
}

function buildTitleLookup(byCode) {
  const byTitleMap = new Map();
  const byNormalizedTitleMap = new Map();

  Object.values(byCode).forEach(entry => {
    entry.titles.forEach(title => {
      mergeLookupEntry(byTitleMap, title, entry);
      mergeLookupEntry(byNormalizedTitleMap, normalizeLookupKey(title), entry);
    });
  });

  return {
    byTitle: sortLookupObject(byTitleMap),
    byNormalizedTitle: sortLookupObject(byNormalizedTitleMap),
  };
}

function mergeLookupEntry(map, key, entry) {
  if (!key) return;

  const current = map.get(key) || {
    movieIds: [],
    titles: [],
    detailMovieId: '',
    detailUrl: '',
    detailCandidates: [],
    hasMultipleDetails: false,
    directorDisplays: [],
    directorLabel: '',
    directorNames: [],
  };

  current.movieIds = uniqueValues(current.movieIds.concat(entry.movieIds || []));
  current.titles = uniqueValues(current.titles.concat(entry.titles || []));
  current.detailMovieId = current.detailMovieId || entry.detailMovieId || '';
  current.detailUrl = current.detailUrl || entry.detailUrl || '';
  current.detailCandidates = mergeDetailCandidates(current.detailCandidates, entry.detailCandidates);
  current.hasMultipleDetails = current.detailCandidates.length > 1;
  current.directorDisplays = uniqueValues(current.directorDisplays.concat(entry.directorDisplays || []));
  current.directorNames = uniqueValues(current.directorNames.concat(entry.directorNames || []));
  current.directorLabel = current.directorDisplays.join(' · ');

  map.set(key, current);
}

function sortLookupObject(map) {
  return Object.fromEntries(
    Array.from(map.entries()).sort(([left], [right]) => left.localeCompare(right, 'ko'))
  );
}

function mergeDetailCandidates(current, next) {
  const merged = (current || []).concat(next || []);
  const seen = new Set();

  return merged.filter(candidate => {
    if (!candidate || !candidate.movieId || seen.has(candidate.movieId)) return false;
    seen.add(candidate.movieId);
    return true;
  });
}

function extractDirectorBlockNames(html) {
  const startIndex = html.indexOf('<div class="director">');

  if (startIndex === -1) return [];

  const slice = html.slice(startIndex, startIndex + 2500);
  const koreanName = cleanText(matchFirst(slice, /<p class="jf-font-3">([\s\S]*?)<\/p>/i));
  const englishName = cleanText(matchFirst(slice, /<p class="jf-font-8[^"]*">([\s\S]*?)<\/p>/i));

  return uniqueValues([koreanName, englishName]);
}

function extractDirectorCreditNames(html) {
  const creditText = cleanText(
    matchFirst(
      html,
      /<span class="jf-label-1">\s*Director\s*<\/span>\s*<span class="jf-label-2">([\s\S]*?)<\/span>/i
    )
  );

  if (!creditText) return [];

  return uniqueValues([creditText].concat(splitNameList(creditText)));
}

function formatDirectorLabel(html, directorNames) {
  const blockNames = extractDirectorBlockNames(html);

  if (blockNames.length === 2) {
    return blockNames.join(' / ');
  }

  if (blockNames.length === 1) {
    return blockNames[0];
  }

  const creditText = cleanText(
    matchFirst(
      html,
      /<span class="jf-label-1">\s*Director\s*<\/span>\s*<span class="jf-label-2">([\s\S]*?)<\/span>/i
    )
  );

  if (creditText) return creditText;

  return directorNames.join(' · ');
}

function splitNameList(value) {
  return value
    .split(/\s*,\s*/)
    .map(item => item.trim())
    .filter(Boolean);
}

function normalizeLookupKey(value) {
  return cleanText(value).toLowerCase();
}

function cleanText(value) {
  return decodeHtml((value || '').replace(/<[^>]+>/g, ' '))
    .replace(/\s+/g, ' ')
    .trim();
}

function matchFirst(text, pattern) {
  const match = text.match(pattern);
  return match ? match[1] : '';
}

function uniqueValues(values) {
  return Array.from(new Set(values.filter(Boolean)));
}

function decodeHtml(value) {
  if (!value) return '';

  return value
    .replace(/&#(\d+);/g, (_, code) => String.fromCodePoint(Number(code)))
    .replace(/&#x([0-9a-f]+);/gi, (_, code) => String.fromCodePoint(parseInt(code, 16)))
    .replace(/&nbsp;/gi, ' ')
    .replace(/&amp;/gi, '&')
    .replace(/&quot;/gi, '"')
    .replace(/&#39;/gi, "'")
    .replace(/&lt;/gi, '<')
    .replace(/&gt;/gi, '>');
}

function buildMovieUrl(movieId) {
  return `${BASE_URL}/db/movieView.asp?idx=${encodeURIComponent(movieId)}`;
}
